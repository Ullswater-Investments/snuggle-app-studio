// Helper function to get all partners as a flat array
export const getAllPartners = (): Partner[] => {
  return partnersByCountry.flatMap((country) => country.partners);
};

// Helper function to get partner by ID
export const getPartnerById = (id: string): Partner | undefined => {
  return getAllPartners().find((partner) => partner.id === id);
};

// Stats
export const getPartnerStats = () => {
  const allPartners = getAllPartners();
  return {
    total: allPartners.length,
    countries: partnersByCountry.length,
    associations: allPartners.filter((p) => p.type === "asociación").length,
    clusters: allPartners.filter((p) => p.type === "clúster").length,
    active: allPartners.filter((p) => p.status === "activo").length,
    inDevelopment: allPartners.filter((p) => p.status === "en desarrollo").length,
    comingSoon: allPartners.filter((p) => p.status === "próximamente").length,
  };
};
